p=Positioner2D;
method='linear';
p.clear();
n=100;
x=1:n;
y=n:-1:1;
t=(1:n)*10;
subplot(2,1,1);
plot(t,x,t,y);
p.GoTo(x,y,t,method);
p.curT=0;
t=t+0.01;
x=x*2;
y=y*2;
p.GoTo(x,y,t,method);
p.curT=0;
t=t-0.005;
x=x*1.5;
y=y*1.5;
p.GoTo(x,y,t,method);
p.setClockRate(200000);
[~,strm]=p.getTimedStream();
[x,y,t]=p.getPathVectors();
subplot(2,1,2);
plot(t,x,t,y);